export function fmt(n) {
  if (n >= 1e6) return '€' + (n / 1e6).toFixed(2) + 'M';
  if (n >= 1e3) return '€' + Math.round(n / 1e3) + 'K';
  return '€' + n;
}

export function fmtN(n) {
  return n.toLocaleString('de-DE');
}